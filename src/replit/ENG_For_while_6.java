package replit;

public class ENG_For_while_6 {
    public static void main(String[] args) {

       /*       Print Alphabet on the console.
                OUTPUT : a b c .. .. .. .. y z
    */
        char ilkHarf = 'a';
        char sonHrf = 'z';
        char sayac = ilkHarf;

        while (sayac <= sonHrf) {

            System.out.print(sayac + " ");
            sayac++;

        }


    }

}
